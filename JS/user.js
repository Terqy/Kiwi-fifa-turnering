class User {
    constructor(id, firstName, lastName, email, password, store, avd, admin, inGroup) {
        this.id = id;
        this.firstName = firstName;
        this.lastName = lastName;
        this.email = email;
        this.password = password;
        this.store = store;
        this.admin = admin;
        this.avd = avd;
        this.inGroup = inGroup;
    }
}